#!/usr/bin/env node
// Generate a Scheme script that compile-files every sandbox demo in the manifest.
const fs = require("fs");

const manifest = fs.readFileSync("sandbox/programs/manifest.sexp", "utf-8");
const re = /name\s+"([^"]+)"\s+file\s+"([^"]+)"/g;
const files = [];
let m;
while ((m = re.exec(manifest)) !== null) {
  files.push("sandbox/programs/" + m[2]);
}

if (files.length === 0) {
  throw new Error("sandbox program manifest did not contain any files");
}

fs.mkdirSync(".tmp", { recursive: true });
const quotedFiles = files.map(file => JSON.stringify(file)).join("\n  ");
fs.writeFileSync(".tmp/sandbox-compile-programs.scm",
  ";;; Auto-generated by scripts/gen-program-compile-scm.js\n" +
  "(for-each compile-file\n" +
  "  '(" + quotedFiles + "))\n");
